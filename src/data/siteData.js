export function getQuoteOfTheDay() {
  const today = new Date()
  const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / 1000 / 60 / 60 / 24)
  return quotes[dayOfYear % quotes.length]
}
